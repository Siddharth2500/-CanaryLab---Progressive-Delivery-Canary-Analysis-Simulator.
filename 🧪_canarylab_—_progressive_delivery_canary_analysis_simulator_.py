# Analyzer
# -----------------------------
@dataclass
class CanaryAnalyzer:
    split: TrafficSplit = field(default_factory=lambda: TrafficSplit(0.9, 0.1))
    sim: MetricSim = field(default_factory=MetricSim)
    history: pd.DataFrame = field(default_factory=pd.DataFrame)

    def run(self, steps: int = 6, batch: int = 1200) -> pd.DataFrame:
        """
        Run multiple sampling windows (steps), each with 'batch' requests.
        """
        frames = []
        for i in range(1, steps + 1):
            df = self.sim.generate(batch, self.split)
            df["step"] = i
            frames.append(df)
        self.history = pd.concat(frames, ignore_index=True)
        return self.history

    def analyze(self,
                error_ratio_limit: float = 1.5,
                max_latency_delta_ms: float = 20.0) -> Dict[str, Any]:
        """
        Decide PASS/FAIL based on:
          - Canary error rate not exceeding baseline by error_ratio_limit (e.g., 1.5x)
          - Canary mean latency not exceeding baseline by max_latency_delta_ms
        """
        if self.history.empty:
            return {"verdict": "UNKNOWN", "reasons": ["No data"]}

        df = self.history
        can = df[df["is_canary"] == True]
        base = df[df["is_canary"] == False]

        # Latencies
        can_lat = can["latency_ms"].values
        base_lat = base["latency_ms"].values
        t_lat, p_lat = welch_t_test(can_lat, base_lat)

        # Errors
        can_err = float(can["error"].mean())
        base_err = float(base["error"].mean())

        # Heuristics
        verdict = "PASS"
        reasons = []

        # Error rate comparison
        if base_err > 0 and (can_err > base_err * error_ratio_limit):
            verdict = "FAIL"
            reasons.append(f"Canary error rate too high ({can_err:.3f} vs {base_err:.3f} baseline)")

        # Latency delta comparison
        lat_delta = float(np.mean(can_lat) - np.mean(base_lat))
        if lat_delta > max_latency_delta_ms:
            verdict = "FAIL"
            reasons.append(f"Canary latency regression (+{lat_delta:.1f} ms > {max_latency_delta_ms} ms limit)")

        # Build summary
        summary = {
            "baseline_latency_ms": float(np.mean(base_lat)),
            "canary_latency_ms": float(np.mean(can_lat)),
            "baseline_error_rate": base_err,
            "canary_error_rate": can_err,
            "t_latency": t_lat,
            "p_latency_approx": p_lat,
            "latency_delta_ms": lat_delta,
            "error_ratio_limit": error_ratio_limit,
            "max_latency_delta_ms": max_latency_delta_ms,
            "verdict": verdict,
            "reasons": reasons
        }
        # Pretty print
        print("=== Canary Analysis ===")
        for k in ["baseline_latency_ms","canary_latency_ms",
                  "baseline_error_rate","canary_error_rate",
                  "t_latency","p_latency_approx","latency_delta_ms",
                  "error_ratio_limit","max_latency_delta_ms","verdict"]:
            print(f"{k}: {summary[k]}")
        if reasons:
            print("reasons:", reasons)
        return summary

    def plot(self, outfile: str = "canary_compare.png"):
        """
        Create a 2-panel figure:
          (1) Latency histograms (baseline vs canary)
          (2) Error rate by step (baseline vs canary)
        """
        df = self.history
        if df.empty:
            print("No history to plot.")
            return

        plt.figure(figsize=(10, 7))

        # Panel 1: Latency histogram
        plt.subplot(2, 1, 1)
        base_lat = df[df.is_canary == False]["latency_ms"]
        can_lat = df[df.is_canary == True]["latency_ms"]
        plt.hist(base_lat, bins=30, alpha=0.6, label="Baseline")
        plt.hist(can_lat, bins=30, alpha=0.6, label="Canary")
        plt.title("Latency Distribution")
        plt.xlabel("Latency (ms)")
        plt.ylabel("Count")
        plt.legend()

        # Panel 2: Error rate by step
        plt.subplot(2, 1, 2)
        err = df.groupby(["step", "is_canary"])["error"].mean().unstack(fill_value=0)
        err.rename(columns={False: "Baseline", True: "Canary"}, inplace=True)
        err.plot(ax=plt.gca())
        plt.title("Error Rate by Step")
        plt.xlabel("Step")
        plt.ylabel("Error Rate")
        plt.tight_layout()
        plt.savefig(outfile, dpi=220)
        print(f"Saved → {outfile}")

