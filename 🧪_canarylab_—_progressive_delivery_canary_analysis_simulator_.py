import time
from dataclasses import dataclass, field
from typing import Dict, Any
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

# -----------------------------
# Reproducibility
# -----------------------------
np.random.seed(int(time.time()) % (2**32 - 1))

# -----------------------------
# Traffic & Metric Simulation
# -----------------------------
@dataclass
class TrafficSplit:
    baseline: float = 0.9
    canary: float = 0.1

    def normalized(self):
        s = self.baseline + self.canary
        if s <= 0:
            return TrafficSplit(0.9, 0.1)
        return TrafficSplit(self.baseline / s, self.canary / s)

@dataclass
class MetricSim:
    base_latency_ms: float = 120.0
    base_error_rate: float = 0.010
    canary_delta_lat_ms: float = 18.0
    canary_delta_error: float = 0.006
    latency_sigma_ms: float = 25.0

    def generate(self, n: int, split: TrafficSplit) -> pd.DataFrame:
        """
        Generate n requests, split between baseline and canary.
        Returns a DataFrame with columns: is_canary, latency_ms, error
        """
        split = split.normalized()
        # Route each request to canary with probability split.canary
        is_canary = np.random.rand(n) < split.canary

        # Base latency distribution
        latency = np.random.normal(self.base_latency_ms, self.latency_sigma_ms, size=n)

        # Base errors (Bernoulli)
        errors = (np.random.rand(n) < self.base_error_rate)

        # Apply deltas for canary traffic
        latency[is_canary] += self.canary_delta_lat_ms
        # Re-draw error for the canary subset using elevated rate
        canary_idx = np.where(is_canary)[0]
        if len(canary_idx) > 0:
            elevated = self.base_error_rate + self.canary_delta_error
            errors[canary_idx] = (np.random.rand(len(canary_idx)) < elevated)

        df = pd.DataFrame({
            "is_canary": is_canary.astype(bool),
            "latency_ms": latency.astype(float),
            "error": errors.astype(int)
        })
        return df

# -----------------------------
# Simple Welch t-test (no SciPy)
# -----------------------------
def welch_t_test(a: np.ndarray, b: np.ndarray):
    """
    Return (t, p_approx) using a normal-approximation for p-value.
    For demo purposes only (not a production-grade stats lib).
    """
    a = np.asarray(a, dtype=float)
    b = np.asarray(b, dtype=float)
    ma, mb = np.mean(a), np.mean(b)
    va, vb = np.var(a, ddof=1), np.var(b, ddof=1)
    na, nb = len(a), len(b)
    denom = np.sqrt(va / max(na,1) + vb / max(nb,1) + 1e-12)
    t = (ma - mb) / denom if denom > 0 else 0.0

    # two-tailed p using Normal(0,1) CDF approximation
    from math import erf, sqrt
    p = 2.0 * (1.0 - 0.5 * (1.0 + erf(abs(t) / sqrt(2.0))))
    return float(t), float(p)

